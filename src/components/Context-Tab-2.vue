      However, &ldquo;&lsquo;raw data&rsquo; is an oxymoron&rdquo;, and it is
      necessary to interrogate what &lsquo;raw&rsquo; data actually
      represents.<sup
        ><a href="#sdfootnote2sym" name="sdfootnote2anc"><sup>2</sup></a></sup
      >
      The measurement, classification and categorisation of any phenomenon into
      &lsquo;data&rsquo; and datasets, which is usable and readable by humans
      and/or machines, is a task of interpretation which always incorporates
      human judgements, subjectivities and biases. The selection of data for
      collection and aggregation, to the categories the data is grouped into,
      and how these categories are ultimately perceived for the purpose of
      making &lsquo;data based decisions&rsquo;, reveal the subjectivities
      inherent in the data that is used within ADMS.
    </p>
