    <p>
      Digital databases need to be organised and structured in particular ways,
      in order to be usable. This process of structuring databases, classifying
      the data and identifying the relationships between the data values, is
      known as data modelling. Many contemporary digital databases are being
      built as &lsquo;relational databases&rsquo;, which is a particular model
      for structuring and retrieving data. Relational Database Management
      Systems or RDBMS afford for discrete databases to be linked and
      information to be retrieved across these databases. RDBMS allows databases
      to be linked through identifying relationships between two or more records
      with common features. Common elements in databases can therefore be linked
      for the purpose of querying or for performing operations across multiple
      disparate databases. More recently, database models like
      <a
        href="https://docs.microsoft.com/en-us/dotnet/architecture/cloud-native/relational-vs-nosql-data"
        >NoSQL</a
      >
      are being utilised to deal with unstructured forms of data, particularly
      &lsquo;real-time&rsquo; data retrieved from multiple sources.
    </p>
    <p>
      Public agencies in India are progressively seeking to merge or create
      linkages across databases which earlier existed in silos. Linking
      disparate databases has been a major use of the Unique Identification
      Number project in India, which has
      <a
        href="https://techcrunch.com/2013/12/06/inside-indias-aadhar-the-worlds-biggest-biometrics-database/"
        >utilised</a
      >
      large relational database solutions including &lsquo;MongoDB&rsquo; and
      &lsquo;MySQL&rsquo; to store and retrieve the data of more than a billion
      Indians. By &lsquo;seeding&rsquo; an Aadhaar number into a pre-existing
      database, the supposedly unique database of UID numbers was linked to
      existing databases. This was done not only to create a unique profile of
      an individual, through their links with various databases, but also to
      identify that each entry within a database of individuals was unique.<sup
        ><a href="#sdfootnote7sym" name="sdfootnote7anc"><sup>7</sup></a></sup
      >
    </p>
    <p>
      Similarly, various databases have been digitised and &lsquo;linked&rsquo;
      to remove duplicates, even in the absence of Aadhaar seeding &ndash; for
      example, the Government of India&rsquo;s subsidy scheme for cooking gas,
      &ldquo;Ujjwala&rdquo;,
      <a href="https://informatics.nic.in/uploads/pdfs/a9fdca31_LPG.pdf"
        >utilises</a
      >
      multiple underlying databases including those provided by cooking gas
      utilities, and the state&rsquo;s own databases of &lsquo;Below Poverty
      Line&rsquo; residents, to identify similar beneficiaries in the databases.
      Similar projects for creating relations between pre-existing databases
      abound within administrative and governance projects. In doing so, ADMS
      also rely on data collected across contexts and across various social
      relationships that individuals may have, which are collapsed into a single
      identity for the purpose of seeing the citizen. These identities then
      become the basis for performing algorithmic operations, and ultimately,
      the basis for administrative decisions.
    </p>

