    <h1>About</h1>

    <p>
      This project was provoked by the marked difference between the progressive
      and hyperbolic discourse on how digital technologies and &lsquo;Artificial
      Intelligence&rsquo; could solve complex problems in governance, and the
      experiences of the people using and being affected by these technologies
      in practice. This is an attempt to both critically analyse the claims of
      AI and Automated Decision Making Systems used in consequential areas, as
      well as inspire greater interrogation of, and participation in,
      conversations about how these systems should be developed and
      governed.&nbsp;
    </p>
